from fastapi import FastAPI, Request, Header, HTTPException
from fastapi.responses import Response
from fastapi.middleware.cors import CORSMiddleware
import httpx
import base64
import io
import colorsys
from datetime import datetime, timezone

try:
    from PIL import Image
    HAS_PIL = True
except ImportError:
    HAS_PIL = False

app = FastAPI()

# Only allow POST requests from Apple Music web -- the Tampermonkey script
# uses GM_xmlhttpRequest which sends with this origin.
app.add_middleware(
    CORSMiddleware,
    allow_origins=["https://music.apple.com"],
    allow_methods=["POST", "GET"],
    allow_headers=["*"],
)

# In-memory state. Resets on server restart, which is fine because
# Tampermonkey pushes a fresh update within INTERVAL_MS of page load.
current_track = {
    "title":       None,
    "artist":      None,
    "artwork_url": None,
    "is_playing":  False,
    "updated_at":  None,
}

try:
    from config import API_SECRET
except ImportError:
    API_SECRET = None


# ---------------------------------------------------------------------------
# Color extraction
# ---------------------------------------------------------------------------

def get_bg_colors(image_bytes):
    """
    Extract three visually distinct colors from the album artwork.

    The palette is quantized to 16 colors, then sorted by vividness
    (saturation * brightness). Colors closer than 36 degrees on the hue
    wheel are skipped so the three blobs look distinct instead of merging.

    If the artwork is monochromatic, missing colors are generated by
    rotating the dominant hue by 120 and 240 degrees.
    """
    if not HAS_PIL:
        return [(220, 50, 60), (110, 40, 190), (230, 120, 0)]
    try:
        img = Image.open(io.BytesIO(image_bytes)).convert("RGB")
        img = img.resize((80, 80), Image.LANCZOS)

        quantized = img.quantize(colors=16)
        palette = quantized.getpalette()[:48]

        candidates = []
        for i in range(16):
            r, g, b = palette[i * 3], palette[i * 3 + 1], palette[i * 3 + 2]
            h, s, v = colorsys.rgb_to_hsv(r / 255, g / 255, b / 255)
            # Skip near-black, near-white, and very desaturated colors
            if s > 0.15 and v > 0.2:
                candidates.append((r, g, b, h, s * v))

        candidates.sort(key=lambda x: -x[4])

        selected = []
        for r, g, b, h, _ in candidates:
            too_close = any(
                min(abs(h - sh), 1 - abs(h - sh)) < 0.1
                for _, _, _, sh in selected
            )
            if not too_close:
                selected.append((r, g, b, h))
            if len(selected) == 3:
                break

        result = [(r, g, b) for r, g, b, _ in selected]

        if not result:
            result = [(220, 50, 60)]
        base_r, base_g, base_b = result[0]
        bh, bs, bv = colorsys.rgb_to_hsv(base_r / 255, base_g / 255, base_b / 255)
        for offset in [0.35, 0.65]:
            if len(result) >= 3:
                break
            h2 = (bh + offset) % 1.0
            r2, g2, b2 = colorsys.hsv_to_rgb(h2, min(bs + 0.1, 0.9), max(bv, 0.5))
            result.append((int(r2 * 255), int(g2 * 255), int(b2 * 255)))

        return result[:3]
    except Exception:
        return [(220, 50, 60), (110, 40, 190), (230, 120, 0)]


